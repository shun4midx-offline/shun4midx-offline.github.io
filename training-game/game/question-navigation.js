function shuffleArray(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}

// Generate question order
export function genQuestionOrder() {
  // Reset 
  localStorage.setItem("curr_order_idx", JSON.stringify(0));

  // Filter questions by level
  const level_questions = questions
    .map((question, idx) => ({ question, idx })) // Include the original index
    .filter(q => q.question.level === JSON.parse(localStorage.getItem("curr_level"))); // Filter by level

  const questionOrder = (JSON.parse(localStorage.getItem("curr_level")) != 4) ? shuffleArray(level_questions.map(q => q.idx)) : level_questions.map(q => q.idx); // Shuffle the original indices

  // Store
  localStorage.setItem("question_order", JSON.stringify(questionOrder));

  // Reset
  localStorage.setItem("chose_order", "true");
}

